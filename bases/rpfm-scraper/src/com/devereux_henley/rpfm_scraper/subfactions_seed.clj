(ns com.devereux-henley.rpfm-scraper.subfactions-seed
  "Reads RPFM `factions_tables.json` and the corresponding loc file, joins
  every playable subfaction back to its parent race via the
  `overrides/faction-key-map` slug prefixes, and emits
  `seed-subfactions.sql`.

  The race FK target is resolved against the hand-written `seed-factions.sql`
  ids — the scraper parses that file to keep its slug → id mapping
  authoritative against whatever seed_factions.sql currently has on disk."
  (:require
   [clojure.java.io :as io]
   [clojure.string :as str]
   [com.devereux-henley.rpfm-scraper.overrides :as overrides])
  (:import
   [java.security MessageDigest]))

(def ^:private seed-author "f0ce7395-a57f-41e9-ade0-fd13bafc058f")
(def ^:private subfaction-uuid-namespace
  "Stable RFC 4122 namespace UUID for subfaction eid generation. Picked once
  and frozen so re-running the scraper produces identical eids for the same
  engine key."
  "1f7c0c3a-fa17-4d6f-8a41-2d3c2c3f5b9c")

(def ^:private faction-row-re
  #"\(\s*(\d+),\s*'[0-9a-f\-]+',\s*\d+,\s*'(?:[^']|'')*',\s*'([^']+)',")

(defn build-slug->faction-id
  "Walk seed-factions.sql and return {slug → faction-id}.  Each VALUES row
  pairs `(id, 'eid', game_id, 'name', 'slug', …)` — we extract id and the
  slug column directly.  Rows without a slug are skipped so callers can fall
  back cleanly when a race isn't yet seeded."
  [seed-dir]
  (let [factions-content (slurp (io/file seed-dir "seed-factions.sql"))]
    (into {}
          (map (fn [m] [(nth m 2) (Long/parseLong (nth m 1))]))
          (re-seq faction-row-re factions-content))))

(defn- faction-slug-for-key
  "Maps an engine factions_tables key (e.g. `wh3_dlc23_chd_legion_of_azgorh`)
  to a race slug (e.g. `chaos-dwarfs`) by scanning for the
  `overrides/faction-key-map` infix prefixes inside the key. Returns nil
  when no prefix matches — those subfactions are skipped."
  [faction-key]
  (some (fn [[slug prefixes]]
          (when (some #(str/includes? faction-key (str "_" % "_")) prefixes)
            slug))
        overrides/faction-key-map))

(defn build-faction-display-name-map
  "{factions_tables key → display name} drawn from `factions_screen_name_<key>`
  loc entries. Falls back to the table row's `screen_name` literal when the
  loc lookup misses."
  [faction-rows faction-loc]
  (let [prefix "factions_screen_name_"]
    (reduce
     (fn [m row]
       (let [k        (get row "key")
             screen   (get row "screen_name")
             from-loc (get faction-loc (str prefix k))]
         (cond
           (and (seq k) (seq from-loc)) (assoc m k from-loc)
           (and (seq k) (seq screen))   (assoc m k screen)
           :else                        m)))
     {}
     faction-rows)))

(defn- uuid-v5
  "Deterministic name-based UUID (RFC 4122 v5, SHA-1) for the given namespace
  + name string. Used so subfaction eids are stable across scraper re-runs."
  [namespace-uuid-str ^String name]
  (let [ns-uuid  (java.util.UUID/fromString namespace-uuid-str)
        ns-bytes (-> (java.nio.ByteBuffer/allocate 16)
                     (.putLong (.getMostSignificantBits ns-uuid))
                     (.putLong (.getLeastSignificantBits ns-uuid))
                     .array)
        digest   (doto (MessageDigest/getInstance "SHA-1")
                   (.update ns-bytes)
                   (.update (.getBytes name "UTF-8")))
        hash     (.digest digest)
        bs       (java.util.Arrays/copyOf hash 16)]
    (aset-byte bs 6 (unchecked-byte (bit-or 0x50 (bit-and (aget bs 6) 0x0f))))
    (aset-byte bs 8 (unchecked-byte (bit-or 0x80 (bit-and (aget bs 8) 0x3f))))
    (let [bb (java.nio.ByteBuffer/wrap bs)]
      (java.util.UUID. (.getLong bb) (.getLong bb)))))

(defn- sql-escape [s] (str/replace (or s "") "'" "''"))

(def ^:private no-rows-sentinel
  "-- no subfaction rows in this scrape (preserved on subsequent empty runs)\n")

(defn- format-subfaction-seed
  "Renders a vector of [id eid key name faction-id] tuples as a multi-row
  INSERT OR REPLACE. Returns the no-rows sentinel for an empty input so the
  file stays well-formed."
  [rows]
  (if (empty? rows)
    no-rows-sentinel
    (let [sorted (sort-by first rows)
          header ["-- Generated by rpfm-scraper.  Do not hand-edit — re-run the scraper."
                  "-- Each row pairs an engine `factions_tables` key with its display name"
                  "-- and parent race (faction).  INSERT OR REPLACE so re-runs converge."
                  "INSERT OR REPLACE INTO subfaction(id, eid, key, name, faction_id, version,"
                  "                                  created_by_sub, created_at, updated_at, deleted_at)"
                  "VALUES"]
          n      (count sorted)
          body   (map-indexed
                  (fn [i [id eid k name faction-id]]
                    (let [comma (if (< (inc i) n) "," ";")]
                      (format "  (%d, '%s', '%s', '%s', %d, 1, '%s', STRFTIME('%%Y-%%m-%%dT%%H:%%M:%%fZ','now'), STRFTIME('%%Y-%%m-%%dT%%H:%%M:%%fZ','now'), null)%s"
                              id eid (sql-escape k) (sql-escape name)
                              faction-id seed-author comma)))
                  sorted)]
      (str (str/join "\n" (concat header body)) "\n"))))

(defn build-subfaction-rows
  "Returns a vector of [id eid key name faction-id] tuples ready for
  format-subfaction-seed. `faction-rows` are the parsed
  factions_tables.json rows; `faction-loc` is the parsed factions_loc.
  Subfactions whose engine key doesn't map to any race-slug prefix
  (uncommon: rebel/no_culture entries, generic placeholders) are skipped."
  [faction-rows faction-loc slug->faction-id]
  (let [name-map (build-faction-display-name-map faction-rows faction-loc)
        sorted   (sort-by #(get % "key") faction-rows)
        kept     (keep
                  (fn [row]
                    (let [k    (get row "key")
                          name (get name-map k)
                          slug (some-> k faction-slug-for-key)
                          fid  (get slug->faction-id slug)]
                      (when (and (seq k) (seq name) fid)
                        [k name fid])))
                  sorted)]
    (vec (map-indexed (fn [i [k name fid]]
                        [(inc i)
                         (str (uuid-v5 subfaction-uuid-namespace k))
                         k name fid])
                      kept))))

(defn generate-subfaction-seed
  "Read factions_tables + factions_loc, slug→id from seed-factions/seed-faction-keys,
  and return the SQL content for seed-subfactions.sql."
  [faction-rows faction-loc seed-dir]
  (let [slug->id (build-slug->faction-id seed-dir)
        rows     (build-subfaction-rows faction-rows faction-loc slug->id)]
    {:content (format-subfaction-seed rows)
     :rows    rows
     :empty?  (empty? rows)}))
